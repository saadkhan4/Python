# Typecasting = The process of converting a variable from one data type to another.
# str(),int(),float(),bool()

name = "Saad Khan"
age = 22
gpa = 3.5
is_student = True

# to know which data type it is.

print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

# to convert into different data types.

gpa = int(gpa)
print(gpa)

name = bool(name)
print(name)


